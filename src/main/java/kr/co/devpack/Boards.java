package kr.co.devpack;


public class Boards {
	private String _id;
	private String _title;
	private String _content;
	private String _file;
	private String _writer;
	private String _write_date;
	
	public String get_id() {
		return _id;
	}
	public void set_id(String _id) {
		this._id = _id;
	}
	public String get_title() {
		return _title;
	}
	public void set_title(String _title) {
		this._title = _title;
	}
	public String get_content() {
		return _content;
	}
	public void set_content(String _content) {
		this._content = _content;
	}
	public String get_file() {
		return _file;
	}
	public void set_file(String _file) {
		this._file = _file;
	}
	public String get_writer() {
		return _writer;
	}
	public void set_writer(String _writer) {
		this._writer = _writer;
	}
	public String get_write_date() {
		return _write_date;
	}
	public void set_write_date(String _write_date) {
		this._write_date = _write_date;
	}
	
}

